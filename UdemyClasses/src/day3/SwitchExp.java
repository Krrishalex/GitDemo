package day3;

public class SwitchExp {
	
	public static void main(String[] args) {
		
		char c='A';
		
		switch(c) {
		case 'A': 
			System.out.println("A");
			break;
		case 'B': 
			System.out.println("B");
			break;
		case 'C': 
			System.out.println("C");
			break;
		case 'D': 
			System.out.println("D");
			break;
		case 'E': 
			System.out.println("E");
			break;
		default:
			System.out.println("Invalid letter");
		}
		
		
	}

}
